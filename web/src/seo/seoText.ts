import type { SeoVariant, CityDef, SubjectDef } from "./catalog";

// Цель: 400–600 слов (для Яндекса и “не тонких” страниц).
// Текст генерируется детерминированно: одинаковые входные данные -> одинаковый текст.
function wordsCount(s: string): number {
  return (s || "").trim().split(/\s+/g).filter(Boolean).length;
}

function hashSeed(s: string): number {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

function pick<T>(arr: T[], seed: number, k: number): T[] {
  if (!arr.length || k <= 0) return [];
  const out: T[] = [];
  let x = seed || 1;
  const used = new Set<number>();
  while (out.length < Math.min(k, arr.length)) {
    x = (Math.imul(1103515245, x) + 12345) >>> 0;
    const idx = x % arr.length;
    if (used.has(idx)) continue;
    used.add(idx);
    out.push(arr[idx]);
  }
  return out;
}

function joinSentences(xs: string[]): string {
  return xs.filter(Boolean).map((x) => x.trim().replace(/\s+/g, " ")).filter(Boolean).join(" ");
}

export function buildSeoText(
  variant: SeoVariant,
  opts: { city?: CityDef | null; subject?: SubjectDef | null }
): { title: string; text: string } | null {
  const city = opts.city ?? null;
  const subject = opts.subject ?? null;

  const place = city ? `в ${city.prepositional}` : "онлайн или в вашем городе";
  const subj = subject ? `по ${subject.dative}` : "по нужному предмету";

  const seedKey = `${variant}|${city?.slug ?? ""}|${subject?.slug ?? ""}`;
  const seed = hashSeed(seedKey);

  const p1 = [
    `Ищете репетитора ${subj} ${place}? На этой странице собраны анкеты преподавателей: опыт, формат занятий, стоимость и контакты для быстрого отклика.`,
    `Здесь удобно сравнить репетиторов ${subj} ${place}: кто работает с вашим уровнем, какие темы закрывает, сколько стоят занятия и как быстро можно начать.`,
    `Подбор репетитора ${subj} ${place} проще, когда всё в одном месте: анкеты, предметы, уровни, форматы и цены.`
  ];

  const p2 = [
    "Сначала определите цель: подтянуть оценки, подготовиться к ОГЭ/ЕГЭ, разобрать пробелы или выйти на высокий балл. Под задачу выбирайте преподавателя по опыту и специализации.",
    "Важно заранее понять, что вы хотите получить: системную подготовку, разбор домашних заданий, разговорную практику или интенсив перед экзаменом. Это помогает быстро сузить выбор.",
    "Если цель понятна, выбирать проще: смотрите опыт, с какими уровнями работает репетитор, какие материалы использует и как строит занятия."
  ];

  const p3 = [
    "Формат занятий тоже влияет на результат. Онлайн удобен, если важна гибкость и выбор преподавателя не ограничен городом. Очные занятия подойдут тем, кому важна атмосфера и контакт вживую.",
    "Обратите внимание на расписание и длительность урока. Часто лучше регулярные занятия 2–3 раза в неделю, чем редкие длинные сессии.",
    "Стоимость обычно зависит от опыта, сложности уровня и формата. Сравнивайте не только цену, но и то, что входит в урок: проверка домашних, материалы, мини‑тесты, обратная связь."
  ];

  const p4 = [
    "Чтобы не ошибиться, начните с короткого знакомства: уточните цель, текущий уровень, сроки и желаемый график. Хороший преподаватель предложит план, объяснит подход и даст первые рекомендации.",
    "Перед стартом задайте 3–5 вопросов: как проходит урок, как даётся домашнее задание, как отслеживается прогресс, что делать при пропусках, как быстро можно получить результат.",
    "Если сомневаетесь между несколькими анкетами, выбирайте по понятности объяснений и структуре. Прозрачный план и регулярная обратная связь обычно важнее громких обещаний."
  ];

  const p5 = [
    "Ниже вы найдёте список анкет. Откройте несколько вариантов, сравните опыт и условия, а затем напишите преподавателю: обычно достаточно 2–3 сообщений, чтобы договориться о пробном уроке.",
    "Посмотрите анкеты ниже, выберите 2–4 кандидата и напишите каждому. Так вы быстро поймёте, кто подходит по стилю общения, цене и расписанию.",
    "Используйте список анкет ниже как каталог: фильтруйте по предметам, городу и формату, а затем связывайтесь с выбранными преподавателями."
  ];

  const title = subject && city
    ? `Как выбрать репетитора по ${subject.dative} в ${city.prepositional}`
    : subject
      ? `Как выбрать репетитора по ${subject.dative}`
      : city
        ? `Как выбрать репетитора в ${city.prepositional}`
        : "Как выбрать репетитора";

  // Собираем 5–6 абзацев, добиваемся 400–600 слов.
  const blocks = [
    joinSentences(pick(p1, seed + 1, 1) as any),
    joinSentences(pick(p2, seed + 2, 1) as any),
    joinSentences(pick(p3, seed + 3, 2) as any),
    joinSentences(pick(p4, seed + 4, 1) as any),
    joinSentences(pick(p5, seed + 5, 1) as any),
  ].filter(Boolean);

  // Если мало слов — добавляем ещё 1–2 предложения из p3/p4.
  let text = blocks.map((b) => b.trim()).join("\n\n");
  let wc = wordsCount(text);

  if (wc < 400) {
    const extra = [
      ...pick(p3, seed + 6, 1),
      ...pick(p4, seed + 7, 1),
    ].map(String);
    text = `${text}\n\n${joinSentences(extra)}`.trim();
    wc = wordsCount(text);
  }

  // Если всё равно мало — добавим финальную строку.
  if (wc < 400) {
    text = `${text}\n\nЕсли хотите ускорить подбор, опишите цель и уровень — так репетитору легче предложить подходящий план занятий.`.trim();
    wc = wordsCount(text);
  }

  // Если слишком много — слегка “подрежем” (редко, но на всякий случай).
  if (wc > 600) {
    const parts = text.split(/\n\n+/g);
    while (wordsCount(parts.join("\n\n")) > 600 && parts.length > 3) {
      parts.splice(parts.length - 2, 1);
    }
    text = parts.join("\n\n");
  }

  return { title, text };
}
